x = int(input("Введіть число x: "))

# Створюємо кортеж A з 20 чисел, кратних x
A = tuple([i * x for i in range(1, 21)])

# Визначаємо кортежі B, C, D
B = A[9:11]  # два числа посередині
C = A[-3:]   # останні три числа
D = A[:4]    # перші чотири числа

# Виводимо результати
print("Кортеж A:", A)
print("Кортеж B (два числа посередині):", B)
print("Кортеж C (останні три числа):", C)
print("Кортеж D (перші чотири числа):", D)
